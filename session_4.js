//increment(++) and decrement(--) operators.
var b=7;
console.log('pre-increment:',++b); 
console.log(b); 

var c=9;
console.log('post-increment:',c++);
console.log(c); 
console.log(c+1); 

var d=5
console.log('post-decrement:',d--); 
console.log(d); 
console.log(d+1); 

var e=3
console.log('pre-decremen:',--e); 
