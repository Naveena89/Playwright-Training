//increment(++) and decrement(--) operators.
var a=7;
console.log('pre-increment:',++a); 
console.log(a); 

var b=9;
console.log('post-increment:',b++);
console.log(b); 
console.log(b+1); 

var c=3
console.log('pre-decrement:',--c); 
console.log(c); 
console.log(c-1); 

var d=5
console.log('post-decrement:',d--); 
console.log(d); 
console.log(d+1); 

